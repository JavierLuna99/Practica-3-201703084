    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        random = (int) (Math.random() * 4);             //Botón desactivar, simulación de la falla de nuestro algoritmo
        if(random == 0){              //Se inhabilita con haciendo uso del proceso 1
            t1.desactivar();
            JOptionPane.showMessageDialog(null, "Proceso 1: Se inhabilitó el acceso al recurso compartido.");
        }
        if(random == 1){              //Se inhabilita con haciendo uso del proceso 2
            t2.desactivar();
            JOptionPane.showMessageDialog(null, "Proceso 2: Se inhabilitó el acceso al recurso compartido.");
        }
        if(random == 2){              //Se inhabilita con haciendo uso del proceso 3
            t3.desactivar();
            JOptionPane.showMessageDialog(null, "Proceso 3: Se inhabilitó el acceso al recurso compartido.");
        }
        if(random == 3){              //Se inhabilita con haciendo uso del proceso 3
            t4.desactivar();
            JOptionPane.showMessageDialog(null, "Proceso 4: Se inhabilitó el acceso al recurso compartido.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

